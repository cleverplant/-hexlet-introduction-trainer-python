# Допустимо использовать отрицательные индексы. 
# В этом случае идёт обращение к символам, начиная с конца строки.
#  -1 — индекс последнего символа, -2 предпоследнего и так далее. 
# В отличие от прямой индексации, в обратной отсчёт идет от -1:
