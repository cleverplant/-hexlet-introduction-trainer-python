# извлечение символов из строки
# extracting characters from a string

first_name = 'Alexander'
print(first_name[0])  # => A

'''Квадратные скобки с цифрой, это операция извлечения элемента по индексу
 — позицией символа внутри строки.
 Индексы начинаются с 0 почти во всех языках программирования — поэтому,
 чтобы получить первый символ, нужно указать индекс 0.
 Индекс последнего элемента равен длине строки минус единица.
 Обращение к индексу за пределами строки приведёт к ошибке:
 IndexError: string index out of range'''

# Длина строки 9, поэтому последний индекс — это 8
first_name = 'Alexander'

print(first_name[8])  # => r
# print(first_name[9]) # => IndexError: string index out of range

