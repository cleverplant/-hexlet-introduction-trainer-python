'''Определение собственных функций значительно упрощает написание 
и поддержку программ. Функции позволяют объединять сложные (составные) 
операции в одну. Например, отправка письма на сайте 
- это достаточно сложный процесс, включающий в себя взаимодействие 
с внешними системами (интернет). Благодаря возможности определять функции, 
вся сложность может быть скрыта за одной простой функцией:'''

# Гипотетический пример
# Место откуда берется функция

'''
from emails import send

email = 'support@hexlet.io'
title = 'Помогите'
body = 'Я написал историю успеха, как я могу получить скидку?'

# Один маленький вызов - и много логики внутри
send(email, title, body)
Внутри себя подобный вызов выполняет довольно много логики. 
Соединяется с почтовым сервером, формирует правильный запрос на основе заголовка 
и тела сообщения, а затем все это отправляет, не забыв закрыть соединение.

Создадим нашу первую функцию. Её задача - вывести на экран приветствие:'''
# Определение функции
# Определение не вызывает и не выполняет функцию
# Мы лишь говорим, что теперь такая функция существует
def show_greeting():
# Внутри тела отступ 4 пробела
    text = 'Hello, Hexlet!'
    print(text)
  # Вызов функции
show_greeting()  # => 'Hello, Hexlet!'

'''
В отличие от обычных данных, функции выполняют действия, 
поэтому их имена практически всегда должны быть глаголами: «построить что-то», 
«нарисовать что-то», «открыть что-то».
Всё, что описывается ниже имени функции с отступом, называется телом функции. 
Внутри тела можно описывать любой код. Считайте, что это маленькая самостоятельная 
программа, набор произвольных инструкций. Тело выполняется ровно в тот момент, 
когда запускается функция. Причём каждый вызов функции запускает тело независимо 
от других вызовов.

Тело функции может быть пустым, в таком случае внутри него используется 
ключевое слово pass:'''

# Минимальное определение функции
def noop():
    pass
noop()

