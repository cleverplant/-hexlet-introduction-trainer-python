first_name = 'Joffrey'
greeting = 'Hello'

# конкатенация - concatenation
print(greeting + ", " + first_name + "!")
# => Hello, Joffrey!

# интерполяция - interpolation
print(f'{greeting}, {first_name}!')
# => Hello, Joffrey!

# Буква f указывает на то, что мы создаём f-строку.
#  f-строка - это шаблон, в который с помощью фигурных скобок
#  подставляются значения переменных. На выходе получается обычная строка.
#  Ещё пример:

school = 'Hexlet'

what_is_it = f'{school} - online courses'
print(what_is_it)  # => Hexlet - online courses

