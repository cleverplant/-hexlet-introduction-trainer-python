# Multi-line строки
text = 'Пример текста,\nсостоящего из\nнескольких строк'
print(text)
# Пример текста,
# состоящего из
# нескольких строк

# Внутри multi-line строки можно переносить текст без 
# использования перевода строки \n.
text = '''Пример текста,
состоящего из
нескольких строк
'''
print(text)
# Обратите внимание на то, что в конце текста есть пустая строка.
#  Эта строка появилась в тексте потому, что мы поставили закрывающие кавычки '''
#  на новой строке.
#  Если не переносить закрывающие кавычки на новую строку,
#  эта пустая строка в тексте не появится.
text = '''Пример текста,
состоящего из
нескольких строк'''
print(text)

# Multi-line строки, из-за использования тройных кавычек,
#  позволяют не экранировать кавычки внутри строки:#
# Здесь не нужно экранировать одинарные кавычки ' и двойные "
# И multi-line строки могут становиться f-строками, для интерполяции:
a = 'A'
b = 'B'

# Слева добавился f
text = f'''{a} и {b}
сидели на трубе
'''
print(text)
